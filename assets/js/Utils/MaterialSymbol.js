export class FontIcon {
    constructor() {
        // this.name = "Font Icon"
        // this.icons = {}

        this.build = function () {
            this.obj = {}

            Object.entries(this.icons).forEach(([key, value]) => {
                this.obj[key] = value
            })

            Object.keys(this.icons).forEach((key) => {
                Object.defineProperty(this.icons, key, {
                    get: () => {
                        return {
                            "name": this.name,
                            "icon": this.obj[key],
                        }
                    }
                })
            })
        }
    }
}

